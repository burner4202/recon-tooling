		<h4>System Indices</h4>
		<br />
		<p>
			CCP is kind enough to allow us to access meta data including system indices, via ESI endpoints.<br /><br />
			System Indexes determine additional fees on various industries in EVE including:

			<br />

			<ul>
				<li>Manufacturing</li>
				<li>Research Time Effiency</li>
				<li>Research Material Effiency</li>
				<li>Copying</li>
				<li>Invention</li>
				<li>Reactions</li>
			</ul>

			<br />

			GSF Recon Tools records the system index data from CCP daily and stores & graphs it for your consumption.

		</p>

		<p>
			So why do we record & graph system indexes? <br /><br />
			Simple, looking at one system index value tells you how much more your going to pay for your reactions, however looking at a number of systems or a region, gives you a bigger picture of whats happening in certain systems. <br /><br />

			Tabbed along the top of this page is a number of examples and how to interpret the data.<br />

		</p>

