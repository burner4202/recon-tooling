		<h4>Scout Tracking</h4>
		<br />
		<p>
			GSF Recon scout tracking is identical to route planning, however, this allows for recon directors to see all registered characters location for scout placement without the need to ping jabber.<br /><br />

			The Following information will be shown:<br />

			<ul>
				<li>Character Name</li>
				<li>Corporation Name</li>
				<li>Current Solar System</li>
				<li>Current Region</li>
				<li>Last Logged In</li>
				<li>Last Logout</li>
				<li>Online/Offline</li>
				<li>No of Logins</li>
				<li>Ship Name</li>
				<li>Ship Type</li>
			</ul>


		</p>
		<p>
			Start by updating all registered characters. <br /><br />
			Select a solar system, GSF Recon will utlise CCP ESI Endpoints to calculate the number of jumps to the selected system. <br />
		</p>

