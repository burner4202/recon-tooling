		<h4>Functionaility</h4>
		<br />
		<p>
			GSF Recon works by initially adding a DSCAN of a target system or multiple DSCANs to aggregate the known upwell structures in a system.<br /><br />

			Once the structures have been import via the DSCAN import to GSF Recon.<br />

			Allocation of the structure owners can be done via ingame metadata, the data is in turned parsed via a structure meta data import.<br /><br />

			Metadata of the structure, which includes:<br />
			<ul>
				<li>Structure ID</li>
				<li>Corporation Name</li>
				<li>Structure Name</li>
			</ul>

			This gives us an overview of the structure, including the Alliance and Corporation.<br />
		</p>
		<p>
			Additionally we can:<br />
			<ul>
				<li>Import fittings (Using a Ship Scanner Module)</li>
				<li>This will tag up the structure for future filtering, including an estimated structure fitting value.</li>
				<li>Add Vulnerability Windows (Using a Data or Relic Analyzer Module.)</li>
				<li>Set States</li>
				<li>High Power / Low Power / Anchoring</li>
				<li>Set Status</li>
				<li>Unanchoring / Reinforce Armor / Reinforce Hull / Clear State.</li>
				<li>Mark as Fitted/Not Fitted</li>
				<li>Destroy</li>
			</ul>
		</p>

		<p>
			All activities are recorded are displayed even upon destroying a structure.<br />
			A full audit log is available for all structures and actions.<br />

		</p>
